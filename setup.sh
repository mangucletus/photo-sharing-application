#!/bin/bash

# Photo Sharing App Setup Script
# This script helps set up the environment after deploying with Terraform

set -e

echo "ğŸš€ Photo Sharing App Setup"
echo "=========================="

# Check if we're in the right directory
if [ ! -f "terraform/main.tf" ]; then
    echo "âŒ Error: Please run this script from the project root directory"
    exit 1
fi

# Check if Terraform is installed
if ! command -v terraform &> /dev/null; then
    echo "âŒ Error: Terraform is not installed"
    echo "Please install Terraform: https://learn.hashicorp.com/tutorials/terraform/install-cli"
    exit 1
fi

# Check if AWS CLI is installed
if ! command -v aws &> /dev/null; then
    echo "âŒ Error: AWS CLI is not installed"
    echo "Please install AWS CLI: https://docs.aws.amazon.com/cli/latest/userguide/getting-started-install.html"
    exit 1
fi

echo "ğŸ“‹ Step 1: Checking AWS credentials..."
if ! aws sts get-caller-identity &> /dev/null; then
    echo "âŒ Error: AWS credentials not configured"
    echo "Please run: aws configure"
    exit 1
fi
echo "âœ… AWS credentials configured"

echo "ğŸ“‹ Step 2: Creating Lambda deployment package..."
if [ ! -d "lambda_package" ]; then
    mkdir lambda_package
fi

echo "Installing Python dependencies..."
pip install -r lambda/requirements.txt -t lambda_package/ --quiet

echo "Copying Lambda function..."
cp lambda/lambda_function.py lambda_package/

echo "Creating deployment package..."
cd lambda_package && zip -r ../image_resizer.zip . > /dev/null
cd ..
echo "âœ… Lambda package created: image_resizer.zip"

echo "ğŸ“‹ Step 3: Deploying infrastructure with Terraform..."
cd terraform

echo "Initializing Terraform..."
terraform init

echo "Planning infrastructure deployment..."
terraform plan -out=tfplan

echo "Applying Terraform configuration..."
terraform apply tfplan

echo "âœ… Infrastructure deployed successfully!"

echo "ğŸ“‹ Step 4: Getting Terraform outputs..."
